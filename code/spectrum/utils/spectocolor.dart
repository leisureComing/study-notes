/// 遵循1931年的CIE颜色匹配系统
/// 1nm的波长
class Spectocolor{
  /// 380-780波段的光谱值
  List spectrums = [];

  /// 总的颜色感觉应是各波长颜色感觉的总和
  final SumColor _sumColor = SumColor();

  /// 380-780波段的色品值常量或刺激值常量
  final CIE1931Color _constColor = CIE1931Color();

  /// 归化后的[x],[y],[z]的三刺激值
  final DomesticationColor _domesticationColor = DomesticationColor();

  /// 色品坐标
  final ColorAxis _colorAxis = ColorAxis();

  /// 转化色品坐标
  /// 严格要求380-780的全部光谱数据
  /// 间隔1nm;必须是从380nm开始
  static ColorAxis conversionColorAxis(List spectrums){
    final Spectocolor spectocolor = Spectocolor._(spectrums);
    spectocolor._orderCalculate();
    return spectocolor._colorAxis;
  }

  /// 转化三刺激值
  /// 严格要求380-780的全部光谱数据
  /// 间隔1nm;必须是从380nm开始
  static SumColor conversionSumColor(List spectrums){
    final Spectocolor spectocolor = Spectocolor._(spectrums);
    spectocolor._orderCalculate();
    return spectocolor._sumColor;
  }

  Spectocolor._(List spectrums){
    this.spectrums = spectrums;
  }

  /// 按顺序进行计算
  void _orderCalculate(){
    // 总色感
    _calculateSumColor();
    // 归化系数
    _calculateConstK();
    // 归化后的三刺激值
    _calculateDomesticationColor();
    // 色品坐标
    _calculateColorAxis();
    // return _colorAxis;
  }

  /// 计算总色感（刺激值）
  void _calculateSumColor(){
    for(int i = 0; i < spectrums.length; i++){
      _sumColor.x += spectrums[i] * _constColor.X[i];
      _sumColor.y += spectrums[i] * _constColor.Y[i];
      _sumColor.z += spectrums[i] * _constColor.Z[i];
    }
  }

  /// 计算归化系数
  void _calculateConstK(){
    _domesticationColor.constK = _domesticationColor.y / _sumColor.y;
  }

  /// 计算归化后的三刺激值
  void _calculateDomesticationColor(){
    _domesticationColor.x = _sumColor.x * _domesticationColor.constK;
    _domesticationColor.z = _sumColor.z * _domesticationColor.constK;
  }

  /// 计算色品坐标
  void _calculateColorAxis(){
    _colorAxis.x = _domesticationColor.x / (_domesticationColor.x + _domesticationColor.y + _domesticationColor.z);
    _colorAxis.y = _domesticationColor.y /  (_domesticationColor.x + _domesticationColor.y + _domesticationColor.z);
    _colorAxis.z = _domesticationColor.z /  (_domesticationColor.x + _domesticationColor.y + _domesticationColor.z);
  }

}


/// 人眼对不同波长的颜色刺激感觉强度不同，
